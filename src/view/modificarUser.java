    private void mostrarEnCampos(int rol) {
        Listas<userCreate> lista = mensajero.consultarUsuarios(rol);
        if (lista != null) {
            try {
                for (int i = 0; i < lista.tamanio(); i++) {
                    txtNombreYapellido.setText(lista.obtener(i).getNombresYapellidos());
                    txtDireccion.setText(lista.obtener(i).getDireccion());
                    txtTelefono.setText(lista.obtener(i).getTelefono());
                    txtEmail.setText(lista.obtener(i).getEmail());
                    txtCedula.setText(String.valueOf(lista.obtener(i).getCedula()));
                    txtCiudad.setText(lista.obtener(i).getCiudad());
                    txtUser.setText(lista.obtener(i).getUser());
                    txtPass.setText(lista.obtener(i).getPass());
                    txtRol.setText(String.valueOf(lista.obtener(i).getRol()));
                }
            } catch (Exception e) {
                System.out.println(e.getCause() +" "+ e.getMessage());
            }
        }
        txtUser.disable();
    }

    private void modificarUser() {
        String NombreyApellido = txtNombreYapellido.getText();
        String direccion = txtDireccion.getText();
        String telefono = txtTelefono.getText();
        String email = txtEmail.getText();
        String cedula = txtCedula.getText();
        String ciudad = txtCiudad.getText();
        String user = txtUser.getText();
        String pass = txtPass.getText();
        String rol = txtRol.getText();

        boolean modificado = mensajero.modificarUser(dt, NombreyApellido, direccion, telefono, email, cedula, ciudad, user, pass, rol);
        if (modificado != false) {
            JOptionPane.showMessageDialog(null, "Los datos han sido modificados!");
            main.irmostrarUser(this, dt);
        } else {
            JOptionPane.showMessageDialog(null, "Los datos no han sido modificados!");

        }
    }

